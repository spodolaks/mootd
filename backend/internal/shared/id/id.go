// Package id provides shared identifier generation utilities.
package id

import (
	"crypto/rand"
	"fmt"
)

// Generate returns a random UUID v4 string using crypto/rand.
func Generate() string {
	b := make([]byte, 16)
	_, _ = rand.Read(b)
	b[6] = (b[6] & 0x0f) | 0x40 // version 4
	b[8] = (b[8] & 0x3f) | 0x80 // variant bits
	return fmt.Sprintf("%08x-%04x-%04x-%04x-%012x",
		b[0:4], b[4:6], b[6:8], b[8:10], b[10:16])
}
